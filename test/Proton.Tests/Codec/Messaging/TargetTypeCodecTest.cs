      [Test]
      public void TestEncodeDecodeArray()
      {
         TestEncodeDecodeArray(false);
      }

      [Test]
      public void TestEncodeDecodeArrayFromStream()
      {
         TestEncodeDecodeArray(true);
      }

      private void TestEncodeDecodeArray(bool fromStream)
      {
         IProtonBuffer buffer = ProtonByteBufferAllocator.Instance.Allocate();
         Stream stream = new ProtonBufferInputStream(buffer);

         Target[] array = new Target[3];

         array[0] = new Target();
         array[1] = new Target();
         array[2] = new Target();

         array[0].Address = "test-1";
         array[0].Dynamic = true;
         array[0].Durable = TerminusDurability.Configuration;
         array[1].Address = "test-2";
         array[1].Dynamic = false;
         array[1].Durable = TerminusDurability.None;
         array[2].Address = "test-3";
         array[2].Dynamic = true;
         array[2].Durable = TerminusDurability.UnsettledState;

         encoder.WriteObject(buffer, encoderState, array);

         object result;
         if (fromStream)
         {
            result = streamDecoder.ReadObject(stream, streamDecoderState);
         }
         else
         {
            result = decoder.ReadObject(buffer, decoderState);
         }

         Assert.IsTrue(result.GetType().IsArray);
         Assert.AreEqual(typeof(Target), result.GetType().GetElementType());

         Target[] resultArray = (Target[])result;

         for (int i = 0; i < resultArray.Length; ++i)
         {
            Assert.IsNotNull(resultArray[i]);
            Assert.IsTrue(resultArray[i] is Target);
            Assert.AreEqual(array[i].Address, resultArray[i].Address);
            Assert.AreEqual(array[i].Dynamic, resultArray[i].Dynamic);
            Assert.AreEqual(array[i].Durable, resultArray[i].Durable);
         }
      }

