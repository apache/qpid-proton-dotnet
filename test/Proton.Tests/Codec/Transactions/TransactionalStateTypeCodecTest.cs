      [Test]
      public void TestEncodeDecodeArray()
      {
         DoTestEncodeDecodeArray(false);
      }

      [Test]
      public void TestEncodeDecodeArrayFromStream()
      {
         DoTestEncodeDecodeArray(true);
      }

      private void DoTestEncodeDecodeArray(bool fromStream)
      {
         IProtonBuffer buffer = ProtonByteBufferAllocator.Instance.Allocate();
         Stream stream = new ProtonBufferInputStream(buffer);

         TransactionalState[] array = new TransactionalState[3];

         array[0] = new TransactionalState();
         array[1] = new TransactionalState();
         array[2] = new TransactionalState();

         array[0].TxnId = ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 0 });
         array[0].Outcome = Accepted.Instance;
         array[1].TxnId = ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 1 });
         array[1].Outcome = Released.Instance;
         array[2].TxnId = ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 2 });
         array[2].Outcome = new Rejected();

         encoder.WriteObject(buffer, encoderState, array);

         object result;
         if (fromStream)
         {
            result = streamDecoder.ReadObject(stream, streamDecoderState);
         }
         else
         {
            result = decoder.ReadObject(buffer, decoderState);
         }

         Assert.IsTrue(result.GetType().IsArray);
         Assert.AreEqual(typeof(TransactionalState), result.GetType().GetElementType());

         TransactionalState[] resultArray = (TransactionalState[])result;

         for (int i = 0; i < resultArray.Length; ++i)
         {
            Assert.IsNotNull(resultArray[i]);
            Assert.IsTrue(resultArray[i] is TransactionalState);
            Assert.AreEqual(array[i].TxnId, resultArray[i].TxnId);
            Assert.AreEqual(array[i].Outcome.GetType(), resultArray[i].Outcome.GetType());
         }
      }

