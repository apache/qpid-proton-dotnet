      private void DoTestSkipValue(bool fromStream)
      {
         IProtonBuffer buffer = ProtonByteBufferAllocator.Instance.Allocate();
         Stream stream = new ProtonBufferInputStream(buffer);

         TransactionalState txnState = new TransactionalState();

         txnState.TxnId = ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 0 });
         txnState.Outcome = Accepted.Instance;

         for (int i = 0; i < 10; ++i)
         {
            encoder.WriteObject(buffer, encoderState, txnState);
         }

         txnState.TxnId = ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 1, 2 });
         txnState.Outcome = null;

         encoder.WriteObject(buffer, encoderState, txnState);

         for (int i = 0; i < 10; ++i)
         {
            if (fromStream)
            {
               IStreamTypeDecoder typeDecoder = streamDecoder.ReadNextTypeDecoder(stream, streamDecoderState);
               Assert.AreEqual(typeof(TransactionalState), typeDecoder.DecodesType);
               typeDecoder.SkipValue(stream, streamDecoderState);
            }
            else
            {
               ITypeDecoder typeDecoder = decoder.ReadNextTypeDecoder(buffer, decoderState);
               Assert.AreEqual(typeof(TransactionalState), typeDecoder.DecodesType);
               typeDecoder.SkipValue(buffer, decoderState);
            }
         }

         object result;
         if (fromStream)
         {
            result = streamDecoder.ReadObject(stream, streamDecoderState);
         }
         else
         {
            result = decoder.ReadObject(buffer, decoderState);
         }

         Assert.IsNotNull(result);
         Assert.IsTrue(result is TransactionalState);

         TransactionalState value = (TransactionalState)result;
         Assert.AreEqual(ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 1, 2 }), value.TxnId);
         Assert.IsNull(value.Outcome);
      }

