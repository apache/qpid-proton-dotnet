      private void DoTestEncodeDecodeType(bool fromStream)
      {
         IProtonBuffer buffer = ProtonByteBufferAllocator.Instance.Allocate();
         Stream stream = new ProtonBufferInputStream(buffer);

         TransactionalState input = new TransactionalState();
         input.TxnId = ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 2, 4, 6, 8 });
         input.Outcome = Accepted.Instance;

         encoder.WriteObject(buffer, encoderState, input);

         TransactionalState result;
         if (fromStream)
         {
            result = (TransactionalState)streamDecoder.ReadObject(stream, streamDecoderState);
         }
         else
         {
            result = (TransactionalState)decoder.ReadObject(buffer, decoderState);
         }

         Assert.AreSame(result.Outcome, Accepted.Instance);

         Assert.IsNotNull(result.TxnId);

         Assert.AreEqual(ProtonByteBufferAllocator.Instance.Wrap(new byte[] { 2, 4, 6, 8 }), result.TxnId);
      }

